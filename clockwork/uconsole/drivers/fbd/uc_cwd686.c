static int _send_buf(const uint8_t* buf, uint32_t len) {
	uint8_t dt;

	if (len == 1) {
		dt = DT_DCS_SHORT_WRITE_0P;
	} else if (len == 2) {
		dt = DT_DCS_SHORT_WRITE_1P;
	} else {
		dt = DT_DCS_LONG_WRITE;
	}
	return uc_dsi_dcs_write(dt, buf, len);
}

int uc_cwd686_init(void) {
	int failures = 0;
	int consec = 0;
	const uint8_t* p = _cwd686_stream;

	/*
	 * cwd686_prepare() order: mipi_dsi_dcs_set_tear_on(VBLANK) FIRST,
	 * then the vendor table (which repeats 0x35 near its end), then
	 * SLPOUT + DSPON.
	 */
	{
		uint8_t tear[2] = { 0x35, 0x00 };
		if (uc_dsi_dcs_write(DT_DCS_SHORT_WRITE_1P, tear, 2) != 0) {
			failures++;
			consec++;
		}
	}

	while (*p != 0) {
		uint8_t len = *p++;
		if (_send_buf(p, len) != 0) {
			failures++;
			consec++;
			/*
			 * Same bail-out as cwu50: three consecutive
			 * TXPKT1_DONE timeouts mean the DSI host itself is
			 * dead, so don't burn the full timeout on the rest.
			 */
			if (consec >= 3) {
				return failures;
			}
		} else {
			consec = 0;
		}
		p += len;
	}

	/* exit_sleep_mode + set_display_on with the DRM driver's settles. */
	{
		uint8_t slpout[1] = { 0x11 };
		uint8_t dspon[1]  = { 0x29 };
		if (uc_dsi_dcs_write(DT_DCS_SHORT_WRITE_0P, slpout, 1) != 0) failures++;
		uc_mdelay(120);
		if (uc_dsi_dcs_write(DT_DCS_SHORT_WRITE_0P, dspon,  1) != 0) failures++;
		uc_mdelay(20);
	}

	return failures;
}
