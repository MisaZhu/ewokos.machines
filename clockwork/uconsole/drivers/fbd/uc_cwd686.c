/*
 * MIPI DSI data types used by this table.  Linux's
 * mipi_dsi_dcs_write_buffer() picks the DT purely from the buffer
 * length (cmd byte included):
 *   1 byte  -- 0x05 DCS short write, no parameter
 *   2 bytes -- 0x15 DCS short write, 1 parameter
 *   >2      -- 0x39 DCS long write
 */
#define DT_DCS_SHORT_WRITE_0P    0x05U
#define DT_DCS_SHORT_WRITE_1P    0x15U
#define DT_DCS_LONG_WRITE        0x39U

